#include <string.h>

char				*ft_strrchr(const char *s, int c)
{
	char	*s2;

	s2 = NULL;
	while (*s != '\0')
	{
		if (*s == (char)c)
			s2 = (char*)s;
		s++;
	}
	if ((char)c == '\0')
		s2 = (char*)s;
	return (s2);
}
